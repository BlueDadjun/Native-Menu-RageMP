class Color {
    public red: number;
    public green: number;
    public blue: number;
    public alpha: number;

    public constructor(red: number = 255, green: number = 255, blue: number = 255, alpha: number = 255) {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.alpha = alpha;
    }
}